package Basics;

public class VariablesBasics {
    public static void main(String[] args) {
        int x = 34;
        int y, z;
        y = 39;
        z = 90;
        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
        x = 121;
        System.out.println(x);
        y = y + 54;
        System.out.println(y);
    }
}
